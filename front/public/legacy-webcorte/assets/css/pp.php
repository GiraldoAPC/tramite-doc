							<h2>About the Cordillera Blanca</h2>
							<p class="txt2">The Cordillera Blanca is an incredible destination for trekking and climbing tours in Peru.  It has abundant native flora and fauna, is culturally rich, has fascinating ruins of several pre-Inca cultures, and preserves some remnants of Inca infrastructure as well.</p>
							<p>The <a href="https://cordillerablanca.info/about-cordillerablanca.php">Cordillera Blanca range</a>,  located in the north of Peru, has a tremendous  concentration of 6000m peaks. The mountains run roughly north to south  and are easily accessible from the Callejon de  Huaylas (Huaylas Valley), which lies to their west.</p>
        <p>In  1975, the Cordillera Blanca or Yurak Janka (in the  Quechua language), &ldquo;The highest tropical mountain range in the world&rdquo;, was  declared <a href="huascaran-national-park.php">Huascaran National Park</a>.  In 1977 and 1985 UNESCO declared this protected area as &ldquo;Biosphere Reserve&rdquo; and  &ldquo;Natural Heritage&rdquo; respectively.</p>
        <p>Between the dramatic peaks of the  Cordillera Blanca the valleys are dotted with stunning turquoise glacial lakes.  Most treks follow these valleys, which run  west to east through the mountains. Among  these mountains and valleys is where most of the adventure travel and tours in the Peruvian  Andes and in the Cordillera de los Andes in South America take place;  activities like: <a href="trekking.php">trekking</a>, hiking, <a href="climbing.php">mountaineering</a>, <a href="climbing.php">climbing</a>,  mountain biking, horse riding and others.</p>
